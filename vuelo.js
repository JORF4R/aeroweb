document.getElementById('flight-search-form').addEventListener('submit', function(e) {
    e.preventDefault();
    
    // Mostrar spinner de carga
    document.getElementById('loading-spinner').style.display = 'block';
    
    // Obtener los valores del formulario
    const origin = document.getElementById('originAirport').value.toUpperCase();
    const destination = document.getElementById('destinationAirport').value.toUpperCase();
    const date = document.getElementById('flightDate').value;
    const status = document.getElementById('flightStatus').value;
    
    // Filtrar los vuelos en base a los criterios seleccionados
    const filteredFlights = flights.filter(flight => {
        return (
            (origin === '' || flight.origin === origin) &&
            (destination === '' || flight.destination === destination) &&
            (date === '' || flight.date === date) &&
            (status === '' || flight.status === status)
        );
    });
    
    // Ocultar spinner de carga
    document.getElementById('loading-spinner').style.display = 'none';
    
    // Limpiar resultados previos
    const resultsBody = document.querySelector('#flight-results tbody');
    resultsBody.innerHTML = '';
    
    // Mostrar los resultados
    if (filteredFlights.length > 0) {
        filteredFlights.forEach(flight => {
            const row = document.createElement('tr');
            row.innerHTML = `
                <td>${flight.number}</td>
                <td>${flight.origin}</td>
                <td>${flight.destination}</td>
                <td>${flight.date}</td>
                <td>${flight.time}</td>
                <td>${flight.status}</td>
            `;
            resultsBody.appendChild(row);
        });
    } else {
        // Si no hay vuelos que coincidan con los filtros
        const row = document.createElement('tr');
        row.innerHTML = `<td colspan="6" class="text-center">No se encontraron vuelos.</td>`;
        resultsBody.appendChild(row);
    }
    });
    